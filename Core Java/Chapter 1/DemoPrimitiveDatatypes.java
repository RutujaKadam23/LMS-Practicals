//WRITE A JAVA PROGRAM TO DEMO ABOUT ALL JAVA PRIMITIVE DATATYPES?

class DemoPrimitiveDatatypes
{
	public static void main(String args[])
	{
		System.out.println("-----Primitive DataTypes-----");
		byte b=127;
		short s=32767;
		int i=2147483647;
		long l=9223372036854775807L;
		float f=1233.7f;
		double d=234345654676545d;
		boolean b1=true;
		char ch='R';
		
		System.out.println("Byte: "+b+"\nShort: "+s+"\nInt: "+i+"\nLong: "+l+"\nFloat: "+f+"\nDouble: "+d+"\nBoolean: "+b1+"\nCharacter: "+ch);
		
	}
}